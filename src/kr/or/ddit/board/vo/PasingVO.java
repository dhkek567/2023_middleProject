package kr.or.ddit.board.vo;

public class PasingVO {
	
	private String pageName;
	private int pagePrice;
	private int pageSale;
	private String pageSize;
	private int pageProperstock;
	
	public String getPageName() {
		return pageName;
	}
	
	public void setPageName(String pageName) {
		this.pageName = pageName;
	}
	
	public int getPagePrice() {
		return pagePrice;
	}
	
	public void setPagePrice(int pagePrice) {
		this.pagePrice = pagePrice;
	}
	
	public int getPageSale() {
		return pageSale;
	}
	
	public void setPageSale(int pageSale) {
		this.pageSale = pageSale;
	}
	
	public String getPageSize() {
		return pageSize;
	}
	
	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}
	
	public int getPageProperstock() {
		return pageProperstock;
	}
	
	public void setPageProperstock(int pageProperstock) {
		this.pageProperstock = pageProperstock;
	}


	
	
	
	
}
