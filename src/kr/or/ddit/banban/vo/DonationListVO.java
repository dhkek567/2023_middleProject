package kr.or.ddit.banban.vo;

import java.util.Date;

public class DonationListVO {

	private String dpNo;
	private String memId;
	private Date dpDate;
	private String dpAmt;
	
	
	public String getDpNo() {
		return dpNo;
	}
	public void setDpNo(String dpNo) {
		this.dpNo = dpNo;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public Date getDpDate() {
		return dpDate;
	}
	public void setDpDate(Date dpDate) {
		this.dpDate = dpDate;
	}
	public String getDpAmt() {
		return dpAmt;
	}
	public void setDpAmt(String dpAmt) {
		this.dpAmt = dpAmt;
	}
	
	
	
	
	
}
